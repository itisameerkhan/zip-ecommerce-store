import mongoose from "mongoose";

const userModel = mongoose.Schema({
  username: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  cart: {
    type: [],
  },
});

export default mongoose.model("User", userModel);
